    public static void centrarFormulario(JFrame frame) {
        // Obtener el tama침o de la pantalla
        Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
        // Obtener el tama침o del formulario
        Dimension frameSize = frame.getSize();
        // Calcular la posici칩n x para centrar el formulario
        int x = (screenSize.width - frameSize.width) / 2;
        // Calcular la posici칩n y para centrar el formulario
        int y = (screenSize.height - frameSize.height) / 2;
        // Establecer la posici칩n del formulario
        frame.setLocation(x, y);
    }
    
    
